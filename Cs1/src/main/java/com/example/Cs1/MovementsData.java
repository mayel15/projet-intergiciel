package com.example.Cs1;

public class MovementsData {
    private String person_id;
    private String numero_sejour;
    private String service;
    private String date_mouvement;

    // Getters and setters

    public String getPerson_id() {
        return person_id;
    }

    public void setPerson_id(String person_id) {
        this.person_id = person_id;
    }

    public String getNumero_sejour() {
        return numero_sejour;
    }

    public void setNumero_sejour(String numero_sejour) {
        this.numero_sejour = numero_sejour;
    }

    public String getService() {
        return service;
    }

    public void setService(String service) {
        this.service = service;
    }

    public String getDate_mouvement() {
        return date_mouvement;
    }

    public void setDate_mouvement(String date_mouvement) {
        this.date_mouvement = date_mouvement;
    }
}
