package com.example.Cs1;

public class StayData {
    private String person_id;
    private String numero_sejour;
    private String date_debut;
    private String date_fin;

    // Getters and setters

    public String getPerson_id() {
        return person_id;
    }

    public void setPerson_id(String person_id) {
        this.person_id = person_id;
    }

    public String getNumero_sejour() {
        return numero_sejour;
    }

    public void setNumero_sejour(String numero_sejour) {
        this.numero_sejour = numero_sejour;
    }

    public String getDate_debut() {
        return date_debut;
    }

    public void setDate_debut(String date_debut) {
        this.date_debut = date_debut;
    }

    public String getDate_fin() {
        return date_fin;
    }

    public void setDate_fin(String date_fin) {
        this.date_fin = date_fin;
    }
}
